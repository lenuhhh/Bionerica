const LangContext = createContext(null)

export function LangProvider({ children }) {
  const [lang, setLang] = useState('en')
  const t = T[lang]
  const currency = CURRENCY[lang]
  const phonePh  = PHONE_PLACEHOLDER[lang]

  /* Convert UAH price → current display currency */
  function price(uah, isUnit = false) {
    const val = lang === 'en' ? (uah * currency.rate).toFixed(2) : Math.round(uah * currency.rate)
    const sym = currency.symbol
    return `${sym}${val}`
  }

  return (
    <LangContext.Provider value={{ lang, setLang, t, currency, phonePh, price }}>
      {children}
    </LangContext.Provider>
  )
}

export function useLang() {
  return useContext(LangContext)
}
